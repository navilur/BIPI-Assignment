		<h1>Welcome to BTEB! </h1>
			<img src="apps/images/building.jpg" alt="" class="logo">

			<p>To cope up with increasing magnitude of academic activities, the need for establishment of a "Statutory Board" was keenly felt. A statutory body namely "The East Pakistan Technical Education Board" was established through Act. No. 1 of 1967 of the then East Pakistan Assembly, which is now Bangladesh Technical Education Board (BTEB).
Thus the Bangladesh Technical Education Board came into existence with the jurisdiction over the entire area of Bangladesh to organize, supervise, regulate, control and develop technical and vocational education. The Board in its present form became operative with effect from June 1969</p>

